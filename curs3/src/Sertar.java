public class Sertar {

  private int lat;
  private int lun;
  private int ina;

  public Sertar(int lat, int lun, int ina) {
    this.lat = lat;
    this.lun = lun;
    this.ina = ina;
  }

  // Tipareste
  public String toString() {
    return "Sertar " + this.lat + " " + this.lun + " " + this.ina;
  }

}
